##########################################################################
# テスト数
##########################################################################
$TESTS = 4

##########################################################################
# テスト実施
##########################################################################
$res = $true
1..$TESTS |
% {
	$n = $_.ToString("000")
	Invoke-Expression ./tests/test${n}.ps1

	if (-Not $?) {
		$res = $false
	}
}

##########################################################################
# テスト結果出力
##########################################################################
$res = $false
if ($res) {
	echo '全テストをパス'
}
else {
	echo 'テスト失敗'
	exit -1
}
